    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
               
        try {
            if(verifyText())
            {
             Integer id=Integer.parseInt(jTextField1.getText());

               Connection con=AllConnection.getConnection();    
               PreparedStatement ps;
        
           int check = JOptionPane.showConfirmDialog(null, "student's result information will be deleted", "Delete result informantion", JOptionPane.OK_CANCEL_OPTION, 0);
            if (check==JOptionPane.OK_OPTION) {
                try {

                    ps = con.prepareStatement("DELETE FROM `result_info` WHERE id = ?");

                    ps.setInt(1, id);

                    if (ps.executeUpdate() > 0) {
                        JOptionPane.showMessageDialog(null, " student's result information deleted");
                    } else {
                        JOptionPane.showMessageDialog(null, " student's result information not deleted");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "add failed");
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
//        s.insertUpdateDelete('d', id, null, null, null);
        jTable1.setModel(new DefaultTableModel(null,new Object[]{"Student id","Semester","Class","Gpa"}));
        s.fillResultJtable(jTable1,"");
        }
        } catch (Exception e) {
//             JOptionPane.showMessageDialog(null,e);
        }
       

    }//GEN-LAST:event_jButton7ActionPerformed

